def make_address_spaces() -> dict:
    """Instantiate all address spaces needed for this experiment."""
    return {
        "semantic":    SemanticAddressSpace(model_name="all-MiniLM-L6-v2"),
        "lexical":     LexicalAddressSpace(),
        "structural":  StructuralAddressSpace(top_k=5),
    }


def make_policies() -> list[Policy]:
    """Return all policies for the structural navigation experiment."""
    return [
        SemanticOnlyPolicy(top_k=5, max_steps=2),
        LexicalOnlyPolicy(top_k=5, max_steps=2),
        StructuralOnlyPolicy(top_k=5),
        EnsembleAllPolicy(top_k=5, max_steps=3),
        StructuralHeuristicPolicy(top_k=5, coverage_threshold=0.45, max_steps=6),
    ]


# ─────────────────────────────────────────────────────────────────────────────
# Dataset loading
# ─────────────────────────────────────────────────────────────────────────────

def load_dataset() -> list[dict]:
    """Generate the structural navigation benchmark (seed=42, 100 examples)."""
    print("Generating Structural Navigation Benchmark (seed=42, N=100) …")
    bench = StructuralNavBenchmark(seed=SEED)
    dataset = bench.generate()
    disc = sum(1 for ex in dataset if ex.get("question_type") == "discovery")
    extr = sum(1 for ex in dataset if ex.get("question_type") == "extraction")
    print(f"  Generated {len(dataset)} examples  "
          f"({disc} discovery, {extr} extraction)\n")
    return dataset


# ─────────────────────────────────────────────────────────────────────────────
# Experiment runner
# ─────────────────────────────────────────────────────────────────────────────

def run_policy(
    policy: Policy,
    dataset: list[dict],
    address_spaces: dict,
    seed: int = SEED,
    max_steps: int = 10,
    token_budget: int = 4000,
) -> dict:
    """Run a single policy on the full dataset and return results."""
    harness = EvaluationHarness(
        address_spaces=address_spaces,
        max_steps=max_steps,
        token_budget=token_budget,
        seed=seed,
    )
    t_start = time.perf_counter()
    result = harness.evaluate(policy, dataset)
    elapsed = time.perf_counter() - t_start
    result["runtime_seconds"] = round(elapsed, 2)
    return result


def run_by_question_type(
    policy: Policy,
    dataset: list[dict],
    address_spaces: dict,
    seed: int = SEED,
) -> dict:
    """Run a policy separately on discovery and extraction subsets."""
    discovery = [ex for ex in dataset if ex.get("question_type") == "discovery"]
    extraction = [ex for ex in dataset if ex.get("question_type") == "extraction"]
    return {
        "discovery":  run_policy(policy, discovery, address_spaces, seed=seed),
        "extraction": run_policy(policy, extraction, address_spaces, seed=seed),
    }


