class StructuralOnlyPolicy(Policy):
    """
    pi_structural: two-phase title navigation.

    Step 0: SEARCH the structural space by title (finds the right document).
    Step 1: OPEN the top-scored title result to retrieve full paragraph content.
    Step 2+: STOP.

    This simulates pure filesystem-browsing: find the file, read it.
    """

    def __init__(self, top_k: int = 5) -> None:
        self._top_k = top_k

    def name(self) -> str:
        return "pi_structural"

    def select_action(self, state: AgentState) -> Action:
        if state.step >= 2 or state.budget_remaining <= 0.10:
            return Action(
                address_space=AddressSpaceType.STRUCTURAL,
                operation=Operation.STOP,
            )

        if state.step == 0:
            # Phase 1: search titles
            return Action(
                address_space=AddressSpaceType.STRUCTURAL,
                operation=Operation.SEARCH,
                params={"query": state.query, "top_k": self._top_k},
            )

        # Phase 2: open the best title match found so far
        # Collect ids from workspace items that came from structural SEARCH
        ids: list[str] = []
        if state.workspace:
            # Take top-scored workspace item from the structural search step
            sorted_ws = sorted(state.workspace, key=lambda x: x.relevance_score, reverse=True)
            ids = [sorted_ws[0].source_id]

        return Action(
            address_space=AddressSpaceType.STRUCTURAL,
            operation=Operation.OPEN,
            params={"query": state.query, "ids": ids},
        )


class EnsembleAllPolicy(Policy):
    """
    pi_ensemble_all: round-robin over semantic → lexical → structural.

    Cycles through the three substrates in fixed order, then STOPs.
    This is the ceiling query-all baseline for the 3-substrate setting.
    """

    _CYCLE = [
        (AddressSpaceType.SEMANTIC,    Operation.SEARCH),
        (AddressSpaceType.LEXICAL,     Operation.SEARCH),
        (AddressSpaceType.STRUCTURAL,  Operation.SEARCH),
    ]

    def __init__(self, top_k: int = 5, max_steps: int = 3) -> None:
        self._top_k = top_k
        self._max_steps = max_steps

    def name(self) -> str:
        return "pi_ensemble_all"

    def select_action(self, state: AgentState) -> Action:
        if state.step >= self._max_steps or state.budget_remaining <= 0.10:
            return Action(
                address_space=AddressSpaceType.SEMANTIC,
                operation=Operation.STOP,
            )

        space_type, operation = self._CYCLE[state.step % len(self._CYCLE)]
        return Action(
            address_space=space_type,
            operation=operation,
            params={"query": state.query, "top_k": self._top_k},
        )


class StructuralHeuristicPolicy(Policy):
    """
    pi_heuristic_struct: heuristic routing extended with structural substrate.

    Routing sequence:
      Step 0: semantic SEARCH (content anchor)
      Step 1: structural SEARCH (title navigation — may find the right document
              when semantic fails on structure-dependent questions)
              If workspace already has ≥2 high-relevance items from ≥2 sources: STOP
      Step 2: lexical fallback or STOP based on coverage
    """

    def __init__(
        self,
        top_k: int = 5,
        coverage_threshold: float = 0.45,
        max_steps: int = 6,
    ) -> None:
        self._top_k = top_k
        self._coverage_threshold = coverage_threshold
        self._max_steps = max_steps

    def name(self) -> str:
        return "pi_heuristic_struct"

    def select_action(self, state: AgentState) -> Action:
        if self._should_stop(state):
            return Action(
                address_space=AddressSpaceType.SEMANTIC,
                operation=Operation.STOP,
            )

        if state.step == 0:
            return Action(
                address_space=AddressSpaceType.SEMANTIC,
                operation=Operation.SEARCH,
                params={"query": state.query, "top_k": self._top_k},
            )

        if state.step == 1:
            return Action(
                address_space=AddressSpaceType.STRUCTURAL,
                operation=Operation.SEARCH,
                params={"query": state.query, "top_k": self._top_k},
            )

        # Step 2+: check diversity, then do lexical fallback or stop
        high_rel = [i for i in state.workspace if i.relevance_score >= 0.4]
        if len(high_rel) >= 2 and len({i.source_id for i in high_rel}) >= 2:
            return Action(
                address_space=AddressSpaceType.SEMANTIC,
                operation=Operation.STOP,
            )

        return Action(
            address_space=AddressSpaceType.LEXICAL,
            operation=Operation.SEARCH,
            params={"query": state.query, "top_k": self._top_k},
        )

    def _should_stop(self, state: AgentState) -> bool:
        if state.step >= self._max_steps:
            return True
        if state.budget_remaining <= 0.10:
            return True
        if not state.workspace:
            return False
        avg_rel = sum(i.relevance_score for i in state.workspace) / len(state.workspace)
        return len(state.workspace) >= 2 and avg_rel >= self._coverage_threshold


