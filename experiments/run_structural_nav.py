_W_POLICY  = 26
_W_RECALL  = 13
_W_OPS     = 8
_W_UTILITY = 14


def _table_header() -> str:
    h = (
        f"| {'Policy':<{_W_POLICY}} "
        f"| {'SupportRecall':>{_W_RECALL}} "
        f"| {'AvgOps':>{_W_OPS}} "
        f"| {'Utility@Budget':>{_W_UTILITY}} |"
    )
    sep = (
        f"| {'-'*_W_POLICY} "
        f"| {'-'*_W_RECALL} "
        f"| {'-'*_W_OPS} "
        f"| {'-'*_W_UTILITY} |"
    )
    return h + "\n" + sep


def _table_row(label: str, agg: dict) -> str:
    return (
        f"| {label:<{_W_POLICY}} "
        f"| {agg['support_recall']:>{_W_RECALL}.4f} "
        f"| {agg['operations_used']:>{_W_OPS}.2f} "
        f"| {agg['utility_at_budget']:>{_W_UTILITY}.4f} |"
    )


def print_results_table(
    title: str,
    results_by_policy: dict[str, dict],
    question_type: Optional[str] = None,
) -> None:
    """Print a formatted results table for one benchmark slice."""
    print(f"\n[{title}]")
    print(_table_header())
    for policy_name, result in results_by_policy.items():
        if question_type is not None:
            agg = result.get(question_type, {}).get("aggregated", {})
        else:
            agg = result.get("aggregated", {})
        if not agg:
            continue
        print(_table_row(policy_name, agg))


def print_stopping_analysis(results_by_policy: dict[str, dict]) -> None:
    """Report stopping patterns and source-diversity stopping behaviour."""
    print("\n[Stopping Pattern Analysis]")
    print(f"  {'Policy':<26}  {'stop_action%':>12}  {'budget%':>8}  {'max_steps%':>10}")
    print(f"  {'-'*26}  {'-'*12}  {'-'*8}  {'-'*10}")
    for pname, result in results_by_policy.items():
        per_ex = result.get("per_example", [])
        n = len(per_ex)
        if n == 0:
            continue
        n_stop   = sum(1 for r in per_ex if r.get("stopped_reason") == "stop_action")
        n_budget = sum(1 for r in per_ex if r.get("stopped_reason") == "budget")
        n_max    = sum(1 for r in per_ex if r.get("stopped_reason") == "max_steps")
        print(
            f"  {pname:<26}  "
            f"{100*n_stop/n:>11.1f}%  "
            f"{100*n_budget/n:>7.1f}%  "
            f"{100*n_max/n:>9.1f}%"
        )


def print_substrate_value_analysis(results_by_policy: dict[str, dict]) -> None:
    """Summarise whether structural adds value over flat retrieval."""
    print("\n[Structural Substrate Value Analysis]")
    u = {pname: r["aggregated"]["utility_at_budget"]
         for pname, r in results_by_policy.items() if "aggregated" in r}
    recall = {pname: r["aggregated"]["support_recall"]
              for pname, r in results_by_policy.items() if "aggregated" in r}

    semantic_u  = u.get("pi_semantic", 0.0)
    lexical_u   = u.get("pi_lexical",  0.0)
    struct_u    = u.get("pi_structural", 0.0)
    ensemble_u  = u.get("pi_ensemble_all", 0.0)
    heuristic_u = u.get("pi_heuristic_struct", 0.0)

    flat_best = max(semantic_u, lexical_u)
    delta_struct_vs_flat = struct_u - flat_best
    delta_ensemble_vs_flat = ensemble_u - flat_best

    print(f"  Best flat retrieval U@B   : {flat_best:.4f}")
    print(f"  Structural-only U@B       : {struct_u:.4f}  (Δ vs flat: {delta_struct_vs_flat:+.4f})")
    print(f"  Ensemble-all U@B          : {ensemble_u:.4f}  (Δ vs flat: {delta_ensemble_vs_flat:+.4f})")
    print(f"  Heuristic+Structural U@B  : {heuristic_u:.4f}")

    recall_s = recall.get("pi_semantic", 0.0)
    recall_l = recall.get("pi_lexical", 0.0)
    recall_struct = recall.get("pi_structural", 0.0)
    recall_ens = recall.get("pi_ensemble_all", 0.0)

    print()
    print(f"  Support recall — semantic   : {recall_s:.4f}")
    print(f"  Support recall — lexical    : {recall_l:.4f}")
    print(f"  Support recall — structural : {recall_struct:.4f}")
    print(f"  Support recall — ensemble   : {recall_ens:.4f}")

    if delta_ensemble_vs_flat > 0.005:
        verdict = "YES — structural navigation adds measurable value beyond flat retrieval."
    elif delta_struct_vs_flat > 0.005:
        verdict = "PARTIAL — structural-only beats flat; ensemble benefit marginal."
    elif delta_struct_vs_flat < -0.005:
        verdict = "NO — flat retrieval outperforms structural navigation on this benchmark."
    else:
        verdict = "NEUTRAL — structural and flat retrieval perform comparably."

    print(f"\n  Q1. Does structural navigation add value? {verdict}")


def print_diversity_stopping_analysis(results_by_policy: dict[str, dict]) -> None:
    """
    Check whether the heuristic diversity-stopping rule (≥2 sources, avg_rel ≥ threshold)
    works correctly across 3 qualitatively different substrates.
    """
    print("\n[Source-Diversity Stopping with 3 Substrates]")
    heuristic_result = results_by_policy.get("pi_heuristic_struct", {})
    agg = heuristic_result.get("aggregated", {})
    per_ex = heuristic_result.get("per_example", [])
    n = len(per_ex)

    if n == 0:
        print("  No heuristic results available.")
        return

    # Compare heuristic vs ensemble (3-substrate ceiling)
    ensemble_result = results_by_policy.get("pi_ensemble_all", {})
    ens_agg = ensemble_result.get("aggregated", {})

    heuristic_u  = agg.get("utility_at_budget", 0.0)
    heuristic_ops = agg.get("operations_used", 0.0)
    ensemble_u   = ens_agg.get("utility_at_budget", 0.0)
    ensemble_ops = ens_agg.get("operations_used", 0.0)

    print(f"  Heuristic+Structural: U@B={heuristic_u:.4f}, avg_ops={heuristic_ops:.2f}")
    print(f"  Ensemble (ceiling):   U@B={ensemble_u:.4f}, avg_ops={ensemble_ops:.2f}")

    efficiency = heuristic_u / max(ensemble_u, 1e-9)
    ops_ratio  = heuristic_ops / max(ensemble_ops, 1e-9)
    print(f"  Efficiency ratio (heuristic / ensemble U@B): {efficiency:.3f}")
    print(f"  Ops ratio (heuristic / ensemble ops): {ops_ratio:.3f}")

    n_early = sum(1 for r in per_ex if r.get("stopped_reason") == "stop_action")
    print(f"  Heuristic early stops: {n_early}/{n} ({100*n_early/n:.1f}%)")

    if efficiency >= 0.85 and ops_ratio <= 0.75:
        verdict = "YES — diversity stopping is Pareto-optimal with 3 substrates."
    elif efficiency >= 0.75:
        verdict = "PARTIAL — diversity stopping efficient but with some U@B loss."
    else:
        verdict = "NO — diversity stopping degrades significantly with 3 substrates."

    print(f"\n  Q2. Does diversity stopping work with 3 substrates? {verdict}")


