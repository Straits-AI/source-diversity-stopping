"""
Structural Navigation Experiment — AEA Framework.

Tests whether a STRUCTURAL address space (title-hierarchy navigation)
changes the stopping picture relative to flat retrieval.

Research questions
------------------
1. Does structural navigation add value that flat retrieval misses?
2. Does source-diversity stopping still work with 3 qualitatively
   different substrates (semantic, lexical, structural)?

Substrates under test
----------------------
  semantic   — dense retrieval (all-MiniLM-L6-v2)
  lexical    — BM25 keyword search
  structural — title-match SEARCH → OPEN hierarchy navigation
  ensemble   — all three substrates queried in round-robin

Policies
---------
  pi_structural      — structural SEARCH only (step 0: title search,
                       step 1: OPEN the top result, then STOP)
  pi_semantic        — semantic SEARCH only (baseline)
  pi_lexical         — lexical SEARCH only (baseline)
  pi_aea_heuristic   — original heuristic (semantic + entity + lexical)
                       adapted to include structural substrate
  pi_ensemble_all    — round-robin: semantic → lexical → structural

Dataset
-------
  100 structure-dependent questions (50 discovery + 50 extraction)
  generated by StructuralNavBenchmark (seed=42, no API calls).

Results saved to experiments/results/structural_nav.json.

Usage
-----
    python experiments/run_structural_nav.py
"""

