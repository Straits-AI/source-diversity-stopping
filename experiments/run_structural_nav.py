def run_structural_nav(
    seed: int = SEED,
    results_path: Optional[Path] = RESULTS_FILE,
) -> dict:
    random.seed(seed)
    np.random.seed(seed)

    print("=" * 70)
    print("STRUCTURAL NAVIGATION EXPERIMENT — AEA Framework")
    print("=" * 70)

    dataset = load_dataset()
    policies = make_policies()

    print("Initialising address spaces …")
    address_spaces = make_address_spaces()
    print("  semantic / lexical / structural ready.\n")

    # ── Run all policies ──────────────────────────────────────────────────────
    print("Running policies …")
    results_by_policy: dict[str, dict] = {}
    subtype_results: dict[str, dict[str, dict]] = {}

    for policy in policies:
        pname = policy.name()
        print(f"  {pname}")

        # Full dataset
        full_result = run_policy(policy, dataset, address_spaces, seed=seed)
        results_by_policy[pname] = full_result

        # Per question-type breakdown
        disc_result = run_policy(
            policy,
            [ex for ex in dataset if ex.get("question_type") == "discovery"],
            address_spaces,
            seed=seed,
        )
        extr_result = run_policy(
            policy,
            [ex for ex in dataset if ex.get("question_type") == "extraction"],
            address_spaces,
            seed=seed,
        )
        subtype_results[pname] = {
            "discovery": disc_result,
            "extraction": extr_result,
        }

        agg = full_result["aggregated"]
        print(
            f"    support_recall={agg['support_recall']:.4f}  "
            f"avg_ops={agg['operations_used']:.2f}  "
            f"utility@budget={agg['utility_at_budget']:.4f}  "
            f"({full_result['runtime_seconds']:.1f}s)"
        )

    # ── Print report ──────────────────────────────────────────────────────────
    print("\n\n" + "=" * 70)
    print("=== STRUCTURAL NAVIGATION RESULTS ===")
    print("=" * 70)

    print_results_table(
        "Overall Results (N=100)",
        {pn: r for pn, r in results_by_policy.items()},
    )

    # Discovery subset
    print_results_table(
        "Discovery Questions (N=50) — 'Which dept handles X?'",
        {pn: subtype_results[pn]["discovery"] for pn in subtype_results},
    )

    # Extraction subset
    print_results_table(
        "Extraction Questions (N=50) — 'What is Y's role in Z?'",
        {pn: subtype_results[pn]["extraction"] for pn in subtype_results},
    )

    print_stopping_analysis(results_by_policy)
    print_substrate_value_analysis(results_by_policy)
    print_diversity_stopping_analysis(results_by_policy)

    # ── Save results ──────────────────────────────────────────────────────────
    payload: dict = {
        "experiment": "structural_navigation",
        "seed": seed,
        "n_examples": len(dataset),
        "by_policy": {
            pname: {
                "aggregated":       result["aggregated"],
                "policy_name":      result["policy_name"],
                "n_examples":       result["n_examples"],
                "n_errors":         result["n_errors"],
                "runtime_seconds":  result["runtime_seconds"],
                "stopped_reasons":  _count_stopped_reasons(result["per_example"]),
            }
            for pname, result in results_by_policy.items()
        },
        "by_question_type": {
            pname: {
                "discovery":  subtype_results[pname]["discovery"]["aggregated"],
                "extraction": subtype_results[pname]["extraction"]["aggregated"],
            }
            for pname in subtype_results
        },
    }

    if results_path is not None:
        results_path.parent.mkdir(parents=True, exist_ok=True)
        with open(results_path, "w", encoding="utf-8") as fh:
            json.dump(payload, fh, indent=2)
        print(f"\nDetailed results saved to: {results_path}")

    return payload


def _count_stopped_reasons(per_example: list[dict]) -> dict[str, int]:
    counts: dict[str, int] = {"stop_action": 0, "budget": 0, "max_steps": 0}
    for r in per_example:
        reason = r.get("stopped_reason", "unknown")
        counts[reason] = counts.get(reason, 0) + 1
    return counts


if __name__ == "__main__":
    run_structural_nav()
